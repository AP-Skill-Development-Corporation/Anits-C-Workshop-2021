//Swap of two numbers
#include <stdio.h>
int main(int argc, char **argv)
{
	int a,b,temp;
	printf("Enter a,b values: ");
	scanf("%d%d",&a,&b);
	temp=a;   //a=a+b;
	a=b;      //b=a-b;
	b=temp;   //a=a-b;
	printf("a=%d\nb=%d",a,b);
	return 0;
}

